public class Ponto {
    
    private int x;
    private int y;
    
    public void setPontoX(int x){
        this.x = x;
    }
    
    public int getPontoX(){
        return x;
    }
    
    
    public void setPontoY(int y){
        this.y = y;
    }
    
    public int getPontoY(){
        return y;
    }
    
   
}